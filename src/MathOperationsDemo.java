public class MathOperationsDemo {
    public static void main(String[] args) {
        int x = 10;
        int y = 5;
        System.out.println("x + y = " + (x + y));
        System.out.println("x - y = " + (x - y));
        System.out.println("x * y = " + (x * y));
        System.out.println("x / y = " + (x / y));

        x++;
        y *= 10;

        System.out.println("x = " + x);
        System.out.println("y = " + y);
    }
}
